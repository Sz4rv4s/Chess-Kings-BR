package model;

/**
 * The Square enum represents the possible states of a square on the game board.
 */
public enum Square {
    /** Represents an empty square on the board. */
    NONE,

    /** Represents a square occupied by the white player. */
    WHITE,

    /** Represents a square occupied by the black player. */
    BLACK,

    /** Represents a blocked (cleared) square on the board. */
    CLEAR
}
